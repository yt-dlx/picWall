import os

def delete_unlisted_files(directory, allowed_files):
    for filename in os.listdir(directory):
        filepath = os.path.join(directory, filename)
        if os.path.isfile(filepath) and filename not in allowed_files:
            try:
                os.remove(filepath)
                print(f"Deleted: {filename} in {directory}")
            except Exception as e:
                print(f"Error deleting {filename} in {directory}: {e}")

